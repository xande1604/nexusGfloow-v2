<p class="mb-4 leading-relaxed">As metas SMART tornam as expectativas mais claras para todos. Descubra como aplicar no RH:</p>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">S - Específicas (Specific)</h2>
<p class="mb-4 leading-relaxed">Defina com clareza o que se espera.</p>
<p class="mb-4 leading-relaxed bg-muted/50 p-4 rounded-lg border-l-4 border-primary"><strong>Exemplo RH:</strong> "Reduzir o turnover do departamento X em 15%."</p>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">M - Mensuráveis (Measurable)</h2>
<p class="mb-4 leading-relaxed">Como a meta será medida?</p>
<p class="mb-4 leading-relaxed bg-muted/50 p-4 rounded-lg border-l-4 border-primary"><strong>Exemplo RH:</strong> "Acompanhar a porcentagem de desligamentos voluntários e involuntários do departamento X mensalmente."</p>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">A - Atingíveis (Achievable)</h2>
<p class="mb-4 leading-relaxed">A meta é realista, considerando os recursos disponíveis?</p>
<p class="mb-4 leading-relaxed bg-muted/50 p-4 rounded-lg border-l-4 border-primary"><strong>Exemplo RH:</strong> "O turnover atual é de 30%; uma redução de 15% é desafiadora, mas alcançável com ações adequadas."</p>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">R - Relevantes (Relevant)</h2>
<p class="mb-4 leading-relaxed">A meta está alinhada aos objetivos maiores da empresa?</p>
<p class="mb-4 leading-relaxed bg-muted/50 p-4 rounded-lg border-l-4 border-primary"><strong>Exemplo RH:</strong> "Reduzir o turnover irá melhorar a produtividade da equipe e reduzir os custos de recrutamento e treinamento."</p>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">T - Temporizáveis (Time-bound)</h2>
<p class="mb-4 leading-relaxed">Qual o prazo para alcançar a meta?</p>
<p class="mb-4 leading-relaxed bg-muted/50 p-4 rounded-lg border-l-4 border-primary"><strong>Exemplo RH:</strong> "A meta deve ser atingida em 6 meses, do dia 01/01/2024 ao dia 30/06/2024."</p>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">O que isso significa para o RH?</h2>
<ul class="list-disc pl-6 mb-6 space-y-2">
  <li><strong>Colaboração em Equipe:</strong> Metas claras levam ao alinhamento.</li>
  <li><strong>Melhora no Desempenho:</strong> Metas específicas motivam resultados.</li>
  <li><strong>Recursos Otimizados:</strong> O foco evita desperdício de tempo e dinheiro.</li>
</ul>

<h2 class="text-2xl font-semibold mt-8 mb-4 text-foreground">Resultados no RH</h2>
<ul class="list-disc pl-6 mb-6 space-y-2">
  <li><strong>Desenvolvimento de Talentos:</strong> Maior engajamento e capacitação.</li>
  <li><strong>Retenção de Colaboradores:</strong> Diminuição do turnover e fuga de talentos.</li>
  <li><strong>Crescimento Organizacional:</strong> Contribuição direta para os resultados do negócio.</li>
  <li><strong>Cultura de Alta Performance:</strong> Melhoria contínua e foco em resultados.</li>
</ul>

<p class="mb-4 leading-relaxed text-lg font-medium">Aplique o método SMART e transforme a gestão de metas do seu RH!</p>'
